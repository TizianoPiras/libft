int	ft_atoi(char *str)
{
	int	ng;
	int	res;

	res = 0;
	ng = 1;
	if (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
		ng = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		res = (10 * res) + (*str - 48);
		str++;
	}
	return ((int)(res * ng));
}

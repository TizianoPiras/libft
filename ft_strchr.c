char	*ft_strchr(const char *s, int c)
{
	char	itoc;
	int		i;

	i = 0;
	itoc = c;
	while (s[i] != '\0' && s[i] != itoc)
		i++;
	if (s[i] == '\0' && itoc != '\0')
		return ((char *)0);
	return ((char *) s + i);
}
